package com.tk.track.vo;

public class UserBehaviorSpecialDataConfigVo {
	
	private String id;
	private String appId;
	private String originalValue;
	private String convertValue;
	private String configDetailId;
	private String clueLevel;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAppId() {
		return appId;
	}
	public void setAppId(String appId) {
		this.appId = appId;
	}
	public String getOriginalValue() {
		return originalValue;
	}
	public void setOriginalValue(String originalValue) {
		this.originalValue = originalValue;
	}
	public String getConvertValue() {
		return convertValue;
	}
	public void setConvertValue(String convertValue) {
		this.convertValue = convertValue;
	}
	public String getConfigDetailId() {
		return configDetailId;
	}
	public void setConfigDetailId(String configDetailId) {
		this.configDetailId = configDetailId;
	}
	public String getClueLevel() {
		return clueLevel;
	}
	public void setClueLevel(String clueLevel) {
		this.clueLevel = clueLevel;
	}
	
}
